# 이스터에그 함수 1

def easterEgg1(num):
    # 모든 입력이 "7534"인 경우 "HELP!" 출력
    if num == 7534:
        print("=> HELP!")

    # 입력이 "777"인 경우 "JACKPOT!" 출력
    elif num == 777:
        print("=> JACKPOT!")

    # 입력이 "404"인 경우 "404 NOT FOUND" 출력
    elif num == 404:
        print("=> 404 NOT FOUND")

    # 입력이 "100"인 경우 "Perfect score!" 출력
    elif num == 100:
        print("=> Perfect score!")

    # 입력이 "0"인 경우 "ZERO" 출력
    elif num == 0 :
        print("=> ZERO")

    return 0
